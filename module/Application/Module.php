namespace Application;

use Zend\Mvc\ModuleRouteListener;
use Zend\Mvc\MvcEvent;
use Zend\Session\SessionManager;

use Zend\Authentication\AuthenticationService;
use User\Listener\AuthenticationListener;
//use Zend\Session\Container;

class Module
{
    public function onBootstrap(MvcEvent $e)
    {
        $eventManager        = $e->getApplication()->getEventManager();
        $moduleRouteListener = new ModuleRouteListener();
        $moduleRouteListener->attach($eventManager);
        
        $sessionManager = new SessionManager();
        $sessionManager->start();


        $ViewModel = $e->getApplication()->getMvcEvent()->getViewModel();

        $Session = $e->getApplication()->getServiceManager()->get('Session');
      
        $ViewModel->setVariable('user_id', $Session->user_id);
        $ViewModel->setVariable('user_name', $Session->user_name);
        $ViewModel->setVariable('count', $Session->count);
    }

    public function getConfig()
    {
        return include __DIR__ . '/config/module.config.php';
    }

    public function getAutoloaderConfig()
    {
        return array(
            'Zend\Loader\StandardAutoloader' => array(
                'namespaces' => array(
                    __NAMESPACE__ => __DIR__ . '/src/' . __NAMESPACE__,
                ),
            ),
        );
    }
}
